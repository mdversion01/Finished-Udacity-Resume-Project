function displaySchool(){
    for (school in education.schools) {

        $('#education').append(HTMLschoolStart);

        var formatEdName = HTMLschoolName.replace("%data%", education.schools[school].name);

        var formatEdDegree = HTMLschoolDegree.replace("%data%", education.schools[school].degree);

        var formatNameDegree = formatEdName + formatEdDegree;

        $('.education-entry:last').append(formatNameDegree);

        var formatEdDates = HTMLschoolDates.replace("%data%", education.schools[school].dates);

        $('.education-entry:last').append(formatEdDates);

        var formatEdLocation = HTMLschoolLocation.replace("%data%", education.schools[school].location);

        $('.education-entry:last').append(formatEdLocation);

        if (education.schools[school].majors.length > 0) {

            for (major in education.schools[school].majors) {
                
                var formatEdMajor = HTMLschoolMajor.replace("%data%", education.schools[school].majors[major]);

                $('.education-entry:last').append(formatEdMajor);
            }
        }

        var href = $('.education-entry a').attr('href');

        var href = href.replace('#', education.schools[school].url);

        $('.education-entry a').attr('href', href);
    }
}

